// 1. Arithmetic Operators عملگرهای حسابی

let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5;

// Concatenation الحاق
x = "Hello" + " " + "World";

// Exponent افزایشی
x = 2 ** 3;

// Increment کاهشی
x = 1;
// x = x + 1;
x++;

// Decrement
// x = x - 1;
x--;

console.log(x);

///////////////////////////////////////////////
// 2. Assignment Operators عملگرهای انتسابی

x = 10;

x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;

console.log(x);

///////////////////////////////////////////////
// 3. Comparison Operators عملگرهای مقایسه ای

// Equal to (Just the value, not the type)
x = 2 == "2";

// Equal to (Type and value)
x = 2 === "2";

// Not equal to (Just the value, not the type)
x = 2 != "2";

// Not equal to (Type and value)
x = 2 !== 2;

// Greater than and less than
x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

console.log(x);

